// single quote inside a double quote
console.log("myName is 'Mohamed'");


// double quote inside a single quote
console.log('myName is "mohamed"');


// double quote inside a double quote
console.log("myName is \"mohamed\"");  // the same thing with the single quote


// to print the backslash itself
console.log("myName \\ is \\ mohamed");


// lineBreak ( \n )
console.log("myName \nis \nmohamed")


// to put a space or tab ( \t )
console.log("\tmyName is mohmed")  // u can use multiple \t